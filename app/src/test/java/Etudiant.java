public class Etudiant {
    private String nom;
    private String classe;
    public Etudiant()
    {
    }
    public void setNom(String nom)
    {
        this.nom=nom;
    }
    public void setClasse(String classe)
    {
        this.classe=classe;
    }

    public String getNom() {
        return nom;
    }
    public String getClasse() {
        return classe;
    }


}
